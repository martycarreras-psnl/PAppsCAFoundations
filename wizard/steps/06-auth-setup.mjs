// wizard/steps/06-auth-setup.mjs — Authentication setup (1Password or .env.local)
import { select, input, password } from '@inquirer/prompts';
import { writeFileSync, readFileSync, existsSync, appendFileSync } from 'node:fs';
import { join } from 'node:path';
import * as ui from '../lib/ui.mjs';
import { stateGet, stateSet, setCompletedStep, TOTAL_STEPS, getRootDir } from '../lib/state.mjs';
import { pacPath, runSafe, runSafeLive } from '../lib/shell.mjs';
import { getClientSecret, setClientSecret } from './05-solution-app-reg.mjs';

export default async function stepAuthSetup() {
  ui.stepHeader(6, TOTAL_STEPS, 'Setting Up Authentication');

  const ROOT = getRootDir();
  const hasOp = stateGet('HAS_OP', false) === true || stateGet('HAS_OP') === 'true';
  const tenantId = stateGet('PP_TENANT_ID');
  const clientId = stateGet('PP_APP_ID');
  const devUrl = stateGet('PP_ENV_DEV');
  const testUrl = stateGet('PP_ENV_TEST', '');
  const prodUrl = stateGet('PP_ENV_PROD', '');

  // Retrieve or re-ask for the client secret (never stored on disk)
  let secret = getClientSecret();
  if (!secret) {
    ui.line('We need your Client Secret again (it\'s not stored on disk).');
    secret = await password({ message: 'Client Secret (hidden input)', validate: (v) => v ? true : 'Required' });
    setClientSecret(secret);
    ui.line('');
  }

  // Auth mode selection
  const choices = [];
  if (hasOp) {
    choices.push({ name: '1Password (secrets never touch disk) — RECOMMENDED', value: '1password' });
  } else {
    choices.push({ name: '1Password — UNAVAILABLE (op CLI not found)', value: '1password', disabled: true });
  }
  choices.push({ name: '.env.local file (secrets on disk, gitignored)', value: 'envlocal' });

  const authMode = await select({ message: 'How do you want to store credentials?', choices });
  stateSet('AUTH_MODE', authMode);

  ui.line('');

  const pac = pacPath();
  const dateStr = new Date().toISOString().slice(0, 10);

  if (authMode === '1password') {
    const appName = stateGet('APP_NAME');
    const vault = await input({ message: '1Password vault name', default: 'Engineering' });
    const itemName = await input({ message: '1Password item name', default: `PowerApps CodeApps - ${appName}` });
    stateSet('OP_VAULT', vault);
    stateSet('OP_ITEM', itemName);

    ui.line('');
    ui.line(`Make sure the 1Password item "${itemName}" exists in`);
    ui.line(`vault "${vault}" with these EXACT field labels:`);
    ui.line('  • tenant-id     (Text)');
    ui.line('  • app-id        (Text)');
    ui.line('  • client-secret (Password)');
    ui.line('  • env-dev       (Text)');
    if (testUrl) ui.line('  • env-test      (Text)');
    if (prodUrl) ui.line('  • env-prod      (Text)');
    ui.line('');
    ui.line('Or create it now with the CLI:');
    ui.line('');
    ui.line(`  op item create \\`);
    ui.line(`    --vault "${vault}" \\`);
    ui.line(`    --category "API Credential" \\`);
    ui.line(`    --title "${itemName}" \\`);
    ui.line(`    "tenant-id[text]=${tenantId}" \\`);
    ui.line(`    "app-id[text]=${clientId}" \\`);
    ui.line(`    "client-secret[password]=YOUR_SECRET" \\`);
    ui.line(`    "env-dev[text]=${devUrl}"`);
    ui.line('');

    // Write .env with op:// references
    let envContent = `# .env — Safe to commit. Contains 1Password references, not secrets.\n`;
    envContent += `# Generated by setup wizard on ${dateStr}\n\n`;
    envContent += `PP_TENANT_ID=op://${vault}/${itemName}/tenant-id\n`;
    envContent += `PP_APP_ID=op://${vault}/${itemName}/app-id\n`;
    envContent += `PP_CLIENT_SECRET=op://${vault}/${itemName}/client-secret\n`;
    envContent += `PP_ENV_DEV=op://${vault}/${itemName}/env-dev\n`;
    if (testUrl) envContent += `PP_ENV_TEST=op://${vault}/${itemName}/env-test\n`;
    if (prodUrl) envContent += `PP_ENV_PROD=op://${vault}/${itemName}/env-prod\n`;
    writeFileSync(join(ROOT, '.env'), envContent, 'utf-8');
    ui.ok('Wrote .env with 1Password references');

    // Create PAC auth profiles
    ui.line('');
    ui.line('Creating PAC auth profiles via 1Password...');
    if (pac) {
      createPacProfile(pac, 'Dev', devUrl, clientId, secret, tenantId);
      if (testUrl) createPacProfile(pac, 'Test', testUrl, clientId, secret, tenantId);
      if (prodUrl) createPacProfile(pac, 'Prod', prodUrl, clientId, secret, tenantId);
    } else {
      ui.warn('PAC CLI not found — skipping auth profile creation.');
    }

  } else {
    // .env.local setup
    ui.line('Writing .env.local...');
    let envContent = `# .env.local — DO NOT commit to Git. Generated by setup wizard on ${dateStr}\n\n`;
    envContent += `PP_TENANT_ID=${tenantId}\n`;
    envContent += `PP_APP_ID=${clientId}\n`;
    envContent += `PP_CLIENT_SECRET=${secret}\n`;
    envContent += `PP_ENV_DEV=${devUrl}\n`;
    if (testUrl) envContent += `PP_ENV_TEST=${testUrl}\n`;
    if (prodUrl) envContent += `PP_ENV_PROD=${prodUrl}\n`;
    writeFileSync(join(ROOT, '.env.local'), envContent, 'utf-8');
    ui.ok('Wrote .env.local');

    // Ensure .gitignore has .env.local
    const gitignorePath = join(ROOT, '.gitignore');
    if (existsSync(gitignorePath)) {
      const gi = readFileSync(gitignorePath, 'utf-8');
      if (!gi.includes('.env.local')) {
        appendFileSync(gitignorePath, '\n.env.local\n');
        ui.ok('Added .env.local to .gitignore');
      } else {
        ui.ok('.env.local is in .gitignore');
      }
    } else {
      writeFileSync(gitignorePath, '.env.local\n', 'utf-8');
      ui.ok('Created .gitignore with .env.local');
    }

    // Create PAC auth profiles
    ui.line('');
    ui.line('Creating PAC auth profiles...');
    if (pac) {
      createPacProfile(pac, 'Dev', devUrl, clientId, secret, tenantId);
      if (testUrl) createPacProfile(pac, 'Test', testUrl, clientId, secret, tenantId);
      if (prodUrl) createPacProfile(pac, 'Prod', prodUrl, clientId, secret, tenantId);
    } else {
      ui.warn('PAC CLI not found — skipping auth profile creation.');
    }
  }

  // Write .env.template (no secrets, always safe)
  ui.line('');
  ui.line('Writing .env.template (no secrets, safe to commit)...');
  let tpl = `# .env.template — Copy to .env.local and fill in values\n`;
  tpl += `# DO NOT commit .env.local to Git\n`;
  tpl += `#\n# For 1Password users: use the .env file (already in the repo) instead.\n\n`;
  tpl += `PP_TENANT_ID=\nPP_APP_ID=\nPP_CLIENT_SECRET=\n`;
  tpl += `PP_ENV_DEV=${devUrl}\n`;
  tpl += `PP_ENV_TEST=${testUrl}\n`;
  tpl += `PP_ENV_PROD=${prodUrl}\n`;
  writeFileSync(join(ROOT, '.env.template'), tpl, 'utf-8');
  ui.ok('Wrote .env.template');

  // Clear secret from memory
  setClientSecret('');

  // Verify connection
  if (pac) {
    ui.line('');
    ui.line('Verifying connection...');
    runSafe(pac, ['auth', 'select', '--name', 'Dev']);
    const who = runSafe(pac, ['org', 'who']);
    if (who) {
      ui.ok('Connected successfully!');
      console.log(who.split('\n').map((l) => `  ${l}`).join('\n'));
    } else {
      ui.line('');
      ui.warn('Connection verification failed.');
      ui.line('Common causes:');
      ui.line('  • App Registration not registered as Application User in this environment');
      ui.line('  • Incorrect Tenant ID, Client ID, or Client Secret');
      ui.line('  • Environment URL is wrong');
    }

    // Check solution
    ui.line('');
    ui.line('Checking for your solution...');
    const solName = stateGet('SOLUTION_UNIQUE_NAME');
    const solList = runSafe(pac, ['solution', 'list']);
    if (solList && solList.toLowerCase().includes(solName.toLowerCase())) {
      ui.ok(`Found solution: ${solName}`);
    } else {
      ui.warn(`Solution "${solName}" not found in solution list.`);
      ui.line('  Make sure you created it in Step 5a and you\'re connected');
      ui.line('  to the right environment.');
    }
  }

  setCompletedStep(6);
}

function createPacProfile(pac, name, url, appId, secret, tenant) {
  const result = runSafe(pac, [
    'auth', 'create',
    '--name', name,
    '--environment', url,
    '--applicationId', appId,
    '--clientSecret', secret,
    '--tenant', tenant,
  ]);
  if (result !== null) {
    ui.ok(`${name} profile created`);
  } else {
    ui.fail(`${name} profile failed — check credentials`);
  }
}
